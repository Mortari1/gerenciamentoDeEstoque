    private void reset(){
        jTextFieldItem.setText(null);
        jTextFieldPrecoCompra.setText(null);
        jTextFieldPrecoVenda.setText(null);
        jTextFieldQuantidade.setText(null);
    }
    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        // TODO add your handling code here:
        Produto produto = new Produto();
        ProdutoDAO inserir = new ProdutoDAO();
        
        try{
            produto.setItem(jTextFieldItem.getText());
            produto.setQtd(Integer.parseInt(jTextFieldQuantidade.getText()));        
            produto.setPrecoCompra(Double.parseDouble(jTextFieldPrecoCompra.getText().replace(",",".")));
            produto.setPrecoVenda(Double.parseDouble(jTextFieldPrecoVenda.getText().replace(",",".")));
            
            inserir.salvarOuAtualizar(produto);
            reset();
        }catch(Exception e){
            e.printStackTrace();
            
        }
        
        
    }//GEN-LAST:event_jButtonEnviarActionPerformed

